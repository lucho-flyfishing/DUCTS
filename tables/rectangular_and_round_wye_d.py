# =========================================================
# BASIC INTERPOLATION
# =========================================================

def interpolate_1d(x, xp, fp):

    if x <= xp[0]:
        return fp[0]

    if x >= xp[-1]:
        return fp[-1]

    for i in range(len(xp) - 1):

        if xp[i] <= x <= xp[i + 1]:

            x1 = xp[i]
            x2 = xp[i + 1]

            y1 = fp[i]
            y2 = fp[i + 1]

            return y1 + (y2 - y1) * (x - x1) / (x2 - x1)


def get_closest_ab_ac_index(Ab_Ac):

    best_index = 0
    best_error = float("inf")

    for i, value in enumerate(AB_AC_ROWS):

        error = abs(value - Ab_Ac)

        if error < best_error:
            best_error = error
            best_index = i

    return best_index


# =========================================================
# MAIN COEFFICIENT
# =========================================================

def get_co_rectangular_and_round_wye_d_main(Vb_Vc):

    return interpolate_1d(
        Vb_Vc,
        VB_VC_MAIN,
        CCS_VALUES
    )


# =========================================================
# BRANCH COEFFICIENT
# =========================================================

def get_co_rectangular_and_round_wye_d_branch(theta_deg, Ab_Ac, Qb_Qc):

    row_index = get_closest_ab_ac_index(Ab_Ac)


    # Select closest angle table

    if theta_deg <= 37.5:
        table = CCB_TABLE_30

    elif theta_deg <= 52.5:
        table = CCB_TABLE_45

    elif theta_deg <= 75:
        table = CCB_TABLE_60

    else:
        table = CCB_TABLE_90
    row_values = table[row_index]

    return interpolate_1d(
        Qb_Qc,
        QB_QC_VALUES,
        row_values
    )