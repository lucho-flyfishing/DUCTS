# =========================================================
# BASIC INTERPOLATION
# =========================================================

def interpolate_1d(x, xp, fp):

    if x <= xp[0]:
        return fp[0]

    if x >= xp[-1]:
        return fp[-1]

    for i in range(len(xp) - 1):

        if xp[i] <= x <= xp[i + 1]:

            x1 = xp[i]
            x2 = xp[i + 1]

            y1 = fp[i]
            y2 = fp[i + 1]

            return y1 + (y2 - y1) * (x - x1) / (x2 - x1)


def get_row_index(target_ab_as, target_ab_ac, rows):

    best_index = 0
    best_error = float("inf")

    for i, row in enumerate(rows):

        error = abs(row[0] - target_ab_as) + abs(row[1] - target_ab_ac)

        if error < best_error:
            best_error = error
            best_index = i

    return best_index


# =========================================================
# BRANCH COEFFICIENT
# =========================================================

def get_co_rectangular_tee_d_branch(Ab_As, Ab_Ac, Qb_Qc):

    row_index = get_row_index(
        Ab_As,
        Ab_Ac,
        BRANCH_ROWS
    )

    row_values = CCB_TABLE[row_index]

    return interpolate_1d(
        Qb_Qc,
        QB_QC_VALUES,
        row_values
    )


